#include "libft.h"

char		*ft_strchr(char *s, int c)
{
	char	p;
	char	*a;
	int		i;

	i = 0;
	a = (char*)s;
	p = c;
	while (a[i] != p)
	{
		if (a[i] == '\0')
		{
			return (NULL);
		}
		i++;
	}
	return ((char*)a + i);
}
